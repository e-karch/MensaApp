/// Enum class for food types
enum FoodType {
  /// the food includes beef
  beef,
  /// the food includes regional beef
  beefAw,
  /// the food includes pork
  pork,
  /// the food includes regional pork
  porkAw,
  /// the food includes fish
  fish,
  /// the food is vegetarian
  vegetarian,
  /// the food is vegan
  vegan,
  /// the food type is unspecified
  unknown,
}